<p>En binaire, on calcul de la même façon qu'en décimal.</p>
<p>
	0 + 0 = 0<br>
	0 + 1 = 1<br>
	1 + 0 = 1<br>
	1 + 1 = 10 (on écrit 0 et on retient 1)
</p>
