<h1>Qu'est-ce qu'un site web ?</h1>
<p>Un site web est un ensemble de pages et de ressources (images, PDF, etc...) hébergés sur un serveur et accessibles via un navigateur web (client).</p>

<h2>Les 3 languages web</h2>
<p>Les trois seuls languages interpretables par le client sont :</p>
<ul>
	<li>HTML (HyperText Markup Language)</li>
	<li>CSS (Cascading Style Sheets)</li>
	<li>JavaScript (JS)</li>
</ul>

<h2>HTML</h2>
<p>Le HTML (HyperText Markup Language) est un langage de balisage. Il sert à structurer le contenu.</p>

<h2>CSS</h2>
<p>Le CSS (Cascading Style Sheets, FR: Feuilles de Style en Cascade). Il sert à la mise en forme du contenu.</p>

<h2>JavaScript</h2>
<p>À en surtout pas confondre avec le Java tout court, le JavaScript, souvent abrégé JS permet d'écrire des scripts pour une page web afin de la rendre plus dynamique.</p>